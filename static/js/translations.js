function applyLanguage() {
    const lang = localStorage.getItem('lang') || 'zh';
    const t = translations[lang] || translations['en'];
    
    // Update document title
    if (t.site_title) {
        document.title = t.site_title;
    }

    // Process simple text elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            // Check for bold markdown
            if (t[key].includes('**')) {
                element.innerHTML = t[key].replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');
            } else {
                element.textContent = t[key];
            }
        } else if (translations['en'][key]) {
             // Fallback to English
             if (translations['en'][key].includes('**')) {
                element.innerHTML = translations['en'][key].replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');
            } else {
                element.textContent = translations['en'][key];
            }
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            element.placeholder = t[key];
        } else if (translations['en'][key]) {
            element.placeholder = translations['en'][key];
        }
    });
    
    // Update language selector value if it exists
    const langSelect = document.getElementById('languageSelect');
    if (langSelect) {
        langSelect.value = lang;
    }
}

