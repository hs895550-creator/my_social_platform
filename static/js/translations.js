function initLanguage() {
    // 1. Get saved lang or default to browser lang or 'en'
    let savedLang = localStorage.getItem('site_lang');
    if (!savedLang) {
        const browserLang = navigator.language.split('-')[0];
        savedLang = translations[browserLang] ? browserLang : 'en';
    }

    // 2. Populate language selector if it exists
    const selector = document.getElementById('lang-selector');
    if (selector) {
        selector.innerHTML = '';
        Object.keys(translations).forEach(code => {
            const option = document.createElement('option');
            option.value = code;
            option.text = translations[code].lang_name;
            if (code === savedLang) option.selected = true;
            selector.appendChild(option);
        });
    }
    
    // 3. Apply translations
    applyTranslations(savedLang);
}

function changeLanguage(lang) {
    localStorage.setItem('site_lang', lang);
    applyTranslations(lang);
}

function applyTranslations(lang) {
    const t = translations[lang] || translations['en']; // Fallback to EN
    
    // Handle text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // Handle placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });

    // Update current lang display if element exists (for index page dropdown)
    const currentLangEl = document.getElementById('currentLang');
    if (currentLangEl) {
        currentLangEl.textContent = "ğŸŒ " + t.lang_name;
    }
}
